    private void jGuardarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGuardarPActionPerformed
        try{
            int id = getPeliculaSeleccionada().getId();
            String nombre = jTFNombreP.getText();
            String fechaLanzamiento = jTFAñoP.getText();
            Date fechaConvertida = (fechaLanzamiento == null || fechaLanzamiento.isEmpty())
                        ? null : dateUtil.convertirStringADate(fechaLanzamiento);
            int duracion = Integer.valueOf(jTFDuracionP.getText());
            String descripcion = jTADescripcionP.getText();
            Categoria categoria = (Categoria)jCBCategoriaP.getSelectedItem();
            
            this.getOperacionesPeli().actualizarPelicula(id, nombre, fechaConvertida, duracion, descripcion, categoria);
            
            JOptionPane.showMessageDialog(this, "Se actualizó correctamente la pelicula",
                "Confirmación de actualización", JOptionPane.INFORMATION_MESSAGE);
        } catch(BDException ex){
            Logger.getLogger(EditarPelicula.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Ocurrio un error al guardar "
                + "la nueva pelicula, por favor contacte al administrador",
                "Error de persistencia de datos", JOptionPane.ERROR_MESSAGE);
        } catch(NumberFormatException | ClassCastException ex){
            Logger.getLogger(EditarPelicula.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Verificar "
                    + "los datos enviados, por favor contacte al administrador",
                "Error de persistencia de datos", JOptionPane.ERROR_MESSAGE);
        } catch (ParseException ex) {
            Logger.getLogger(EditarPelicula.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "La fecha de lanzamiento de la "
                    + "pelicula seleccionada es invalida, por favor contacte al administrador",
                    "Error en fecha de lanzamiento", JOptionPane.ERROR_MESSAGE);
        }                 
    }//GEN-LAST:event_jGuardarPActionPerformed

