    public void setPeliculaSeleccionada(Pelicula pelicula){
        this.peliculaSeleccionada = pelicula;
        cargarPeliculaSeleccionada(peliculaSeleccionada);
    }
    
    private Pelicula getPeliculaSeleccionada(){
        return this.peliculaSeleccionada;
    }
    
    private int getIndiceCategoria(String categoriaS){
        int indice = 0;
        for (int i = 0; i < jCBCategoriaP.getItemCount(); i++) {
            Categoria categoriaCB = (Categoria)jCBCategoriaP.getItemAt(i);
            if(categoriaS.equals(categoriaCB.getNombre())){
                indice = i;
                break;
            }
        }
        return indice;
    }
    
    private void cargarPeliculaSeleccionada(Pelicula peliculaS){
        String fechaConvertida = (peliculaS.getAño() == null) ? "" : dateUtil.convertirDateAString(peliculaS.getAño());
        String descripcionConvertida = (peliculaS.getDescripcion() == null || 
                "null".equals(peliculaS.getDescripcion())) ? "" : peliculaS.getDescripcion();
        
        int indiceCategoria = this.getIndiceCategoria(peliculaS.getCategoria().getNombre());
        jTFNombreP.setText(peliculaS.getNombre());
        jTFDuracionP.setText(String.valueOf(peliculaS.getDuracion()));
        jTFAñoP.setText(fechaConvertida);
        jTADescripcionP.setText(descripcionConvertida);
       
        jCBCategoriaP.setSelectedIndex(indiceCategoria);
    }
            
    private void cargarCategorias(){
        try{
            categorias = this.getOperacionesPeli().obtenerAllCategorias();
            for(Categoria categoria: categorias){
                this.jCBCategoriaP.addItem(categoria);
            }
        }catch(BDException ex){
            JOptionPane.showMessageDialog(this, "Ocurrio un error al consultar "
                    + "las categorias, mensaje: " + ex.getMessage(), 
                    "Error de persistencia de datos", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private OperacionesPeliculas getOperacionesPeli() {
        if(operacionesPeli == null){
            operacionesPeli = new OperacionesPeliculasImpl();
        }
        return operacionesPeli;
    }
    
